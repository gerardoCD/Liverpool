// MARK: - ProductModel
struct ProductModel: Codable {
    let status: Status
    let pageType: String
    let plpResults: PlpResults
}

// MARK: - PlpResults
struct PlpResults: Codable {
    let records: [Record]
}

// MARK: - Navigation
